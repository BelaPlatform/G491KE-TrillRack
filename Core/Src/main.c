static void resetUsbDp()
{
  // temporarily set USB_DP pin (PA12) low to force the host to re enumerate it
  // (it will be set high again by MX_USB_DEVICE_Init() below)
  __HAL_RCC_GPIOA_CLK_ENABLE();
  GPIO_InitTypeDef GPIO_InitStructure;
  GPIO_InitStructure.Pin = GPIO_PIN_12;
  GPIO_InitStructure.Mode = GPIO_MODE_OUTPUT_PP;
  GPIO_InitStructure.Speed = GPIO_SPEED_FREQ_LOW;
  HAL_GPIO_Init(GPIOA, &GPIO_InitStructure);
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_12, GPIO_PIN_RESET);
  for(volatile unsigned int i = 0; i < 1 * 1000 * 1000; ++i)
    ; // wait (about 74ms) for the host to recognise the change
}
/* USER CODE END 0 */

/**
  * @brief  The application entry point.
  * @retval int
  */

int main(void)
{
  /* USER CODE BEGIN 1 */
#ifdef CFG_BOOTLOADER
  // minimal initialisation to:
  // - keep code size to a minimum
  // - ensure there is nothing else initialised by the time we jump
  HAL_Init();
  SystemClock_Config();
  MX_RTC_Init();
  BootloaderResetDest_t to = bootloaderGetDest(&hrtc);
  // NOTE: we jump to bootloader _after_ toggling the USB_DP pin so that if we end up
  // jumping to system bootloader, the host will be able to detect DFU
  resetUsbDp();
  // borrowed from MX_GPIO_Init() --- start
  /*Configure GPIO pins : SW_LED_A_Pin SW_LED_B_Pin DEBUG2_Pin DEBUG3_Pin */
  __HAL_RCC_GPIOB_CLK_ENABLE();
  GPIO_InitTypeDef GPIO_InitStruct = {0};
  GPIO_InitStruct.Pin = SW_LED_A_Pin|SW_LED_B_Pin|DEBUG2_Pin|DEBUG3_Pin;
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
  GPIO_InitStruct.Pull = GPIO_NOPULL;
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
  HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);
  // borrowed from MX_GPIO_Init() --- stop
  GPIO_PinState swa;
  GPIO_PinState swb;
  switch(to)
  {
  default:
  case kBootloaderMagicUserApplication:
	  // green
	  swa = GPIO_PIN_SET;
	  swb = GPIO_PIN_RESET;
	  break;
  case kBootloaderMagicSystemBootloader:
  case kBootloaderMagicUserFlasher:
	  // red
	  swa = GPIO_PIN_RESET;
	  swb = GPIO_PIN_SET;
	  break;
  }
  HAL_GPIO_WritePin(GPIOB, SW_LED_A_Pin, swa);
  HAL_GPIO_WritePin(GPIOB, SW_LED_B_Pin, swb);
  bootloaderJump(&hrtc, to);
  while(1)
	  ;
#else //!CFG_BOOTLOADER
  bootloaderSetVector();
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  // initialise the bare minimum to run the neopixels
  // so we can turn them all off ASAP
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_TIM2_Init();
#ifndef CFG_FLASHER
  TrillRackApplication_earlyInit();
#endif //CFG_FLASHER
  resetUsbDp();
  /* USER CODE END SysInit */
  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();
  MX_ADC2_Init();
  MX_DAC1_Init();
  MX_TIM2_Init();
  MX_I2C2_Init();
  MX_TIM6_Init();
  MX_TIM1_Init();
  MX_USB_Device_Init();
  MX_RTC_Init();
  /* USER CODE BEGIN 2 */
  RetargetInit(&huart2);
  printf("Booting %s: %s\n\r", bootloaderIsFlasher() ? "flasher" : "application", kVerificationBlock.stringId);
  midiInit();

#ifdef CFG_FLASHER
  printf("Making PSoC pins available for debugging\n\r");
  i2cPinsMode(kI2cPinsModeExternal);
  // red
  HAL_GPIO_WritePin(GPIOB, SW_LED_A_Pin, GPIO_PIN_RESET);
  HAL_GPIO_WritePin(GPIOB, SW_LED_B_Pin, GPIO_PIN_SET);
#else //CFG__FLASHER
  int ret = TrillRackApplication();
  printf("TrillRackApplication returned %d\n", ret);
  Error_Handler();
#endif //CFG_FLASHER
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
#ifdef CFG_FLASHER
    processMidiMessage();
#endif // CFG_FLASHER
    /* USER CODE BEGIN 3 */
  }
#endif // !CFG_BOOTLOADER
  /* USER CODE END 3 */
}

